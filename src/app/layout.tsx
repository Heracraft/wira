import type { Metadata } from "next";
import { headers } from "next/headers";

import { createClient } from "@/lib/store.server";

import { Toaster } from "@/components/ui/sonner";
import AuthProvider from "@/components/AuthProvider";

import Navbar from "@/components/navbar";
import Footer from "@/components/footer";

import "./globals.css";

import { User } from "@/types/auth";

export const metadata: Metadata = {
	title: "Create Next App",
	description: "Generated by create next app",
};

export default async function RootLayout({
	children,
}: Readonly<{
	children: React.ReactNode;
}>) {
	const headersList = await headers();
	const pathname = headersList.get("x-pathname");

	const client = await createClient(); //server client

	const {
		data: { user },
	} = await client.auth.getUser();

	console.log(user);

	let initialUserState = null;

	if (user && !pathname?.startsWith("/auth")) {
		initialUserState = {
			email: user.email,
			id: user.id,
		} as User;
	}

	return (
		<html lang="en">
			<body className="antialiased bg-neutral-100 min-h-[100dvh] flex flex-col">
				<Navbar />
				<AuthProvider initialState={user} />
				<div className="flex-1 py-5 flex flex-col">{children}</div>
				{(pathname?.startsWith("/auth") || pathname?.startsWith("/onboarding")) ? null : <Footer />}
				<Toaster />
			</body>
		</html>
	);
}
